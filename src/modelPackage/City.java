package modelPackage;

public class City {
    private String id;
    private int zipCode;
    private String name;
    private String idCountry;

    // CONSTRUCTOR

    public City() {}

    // GETTER

    public String getId() {
        return id;
    }

    public int getZipCode() {
        return zipCode;
    }

    public String getName() {
        return name;
    }

    public String getIdCountry() {
        return idCountry;
    }
}
